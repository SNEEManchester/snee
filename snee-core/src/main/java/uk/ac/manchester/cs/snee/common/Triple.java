package uk.ac.manchester.cs.snee.common;

import java.io.Serializable;

public class Triple<S1, S2, S3> implements Serializable{

    /**
   * serialVersionUID
   */
  private static final long serialVersionUID = -7328023396239587100L;

    S1 s1;

    S2 s2;

    S3 s3;

    public Triple(S1 s1, S2 s2, S3 s3) {
	this.s1 = s1;
	this.s2 = s2;
	this.s3 = s3;
    }

    public S1 getFirst() {
	return this.s1;
    }

    public S2 getSecond() {
	return this.s2;
    }

    public S3 getThird() {
	return this.s3;
    }

}
